# La palabra Mississippi también se usa con un propósito ligeramente diferente: para contar "mississippily" (a la manera de Mississippi).
# Si no estás familiarizado con la frase, estamos aquí para explicarte lo que significa: se utiliza para contar los segundos.
# La idea detrás de esto es que agregar la palabra Mississippi a un número al contar los segundos en voz alta hace que suene más cercano al reloj, y por lo tanto "uno Mississippi, dos Mississippi, tres Mississippi" tomará aproximadamente tres segundos reales. 
# A menudo lo usan los niños que juegan a las escondidas para asegurarse de que quien cuenta lo haga de manera justa.
# Tu tarea aquí es muy simple: escribe un programa que use un bucle for para "contar al estilo Mississippi" hasta cinco. 
