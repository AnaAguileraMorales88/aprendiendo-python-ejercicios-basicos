# Com fer un rectangle
print("+" + 10 * "-" + "+")
print(("|" + " " * 10 + "|\n") * 5, end="")
print("+" + 10 * "-" + "+")

# Com fer una fletxa
# Cap de la fletxa
print(" " * 5 + "*")
print(" " * 6 + "*" * 2)
print(" " * 7 + "*" * 3)

# Cos de la fletxa
print("*" * 10)

# Cap de la fletxa (invertit)
print(" " * -5 + "*" * 3)
print(" " * -6 + "*" * 2)
print(" " * -7 + "*")